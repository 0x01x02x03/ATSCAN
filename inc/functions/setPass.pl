  our (@AUTH, @c, $scriptPass);
  if (-e $scriptPass) {
    print $c[2]."[!] $AUTH[2]$c[10] ";
    my $askMe=<STDIN>;
    chomp ($askMe);
    if($askMe=~/(y|Y)/) {
      chomp $askMe; unlink $scriptPass; print $c[3]."[!] $AUTH[3]\n";
    }else{ logoff(); }       
  }else{
    desclaimer();
    print $c[4]."\n[!] $AUTH[4]\n".$c[4]."[!] $AUTH[5]:$c[10] ";
    my $ps=<STDIN>;
    chomp ($ps);
    if ($ps eq "") { print $c[4]."[!] Password cannot be empty!\n"; logoff(); }
    $ps=Digest::MD5->md5_hex($ps);
    printFile($scriptPass, $ps);
    print $c[3]."[!] $AUTH[6]\n";
  } logoff();
