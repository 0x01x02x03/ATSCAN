  our (@AUTH, @c, $scriptPass, $script);
  if (-e $scriptPass) {
    print $c[2]."[!] $AUTH[2]$c[10] ";
    my $askMe=<STDIN>;
    chomp ($askMe);
    if($askMe=~/(y|Y)/) {
      chomp $askMe; unlink $scriptPass; sleep(2); print $c[3]."[!] $AUTH[3]\n";
    }else{ logoff(); }       
  }else{
    print $c[4]."\n[!] $AUTH[4]\n".$c[4]."[!] $AUTH[5]:$c[10] ";
    my $ps=<STDIN>;
    chomp ($ps);
    if ($ps eq "") { print $c[4]."[!] Password cannot be empty!\n"; logoff(); }
    $ps=Digest::MD5->md5_hex($ps);
    printFile($scriptPass, $ps);   
    use Time::HiRes qw(usleep);
    local $| = 1;
    my @nums = -5 .. -1;
    print $c[3]."[!] $AUTH[6] Tool will restart in ";
    foreach my $c (@nums) {
      print "$c";
      usleep(1000000);
      print ("\b" x length($c));
    }
    print "\n";
    system("perl $script || atscan");
  } logoff();
######################################################################################################################################################################################################
